package com.ict01.grammar02;

class Ex01 {
	public static void main(String[] args) {
		//논리형: boolean, "true 또는 false" 만 저장
		//자료형  변수이름 ;
		//변수이름 = 데이터 ;
		
		boolean bo1; //변수 선언
		bo1 = true;

		//변수 호출
		System.out.println(bo1); //변수가 가지고 있는 데이터를 내보냄

		bo1 = false;
		System.out.println(bo1);

		boolean bo2 = true;
		System.out.println(bo2);
	}
}